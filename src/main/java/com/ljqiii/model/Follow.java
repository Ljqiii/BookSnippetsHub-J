package com.ljqiii.model;

public class Follow {

    int id;
    String openid;
    String followopenid;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getOpenid() {
        return openid;
    }

    public void setOpenid(String openid) {
        this.openid = openid;
    }

    public String getFollowopenid() {
        return followopenid;
    }

    public void setFollowopenid(String followopenid) {
        this.followopenid = followopenid;
    }
}
